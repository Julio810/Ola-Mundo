dia = int(input('Digite um número correspondente ao dia da semana [1 - 7]: '))
if dia == 1:
  print('DOMINGO')
elif dia == 2:
  print('SEGUNDA-FEIRA')
elif dia == 3:
  print('TERÇA-FEIRA')
elif dia == 4:
  print('QUARTA-FEIRA')
elif dia == 5:
  print('QUINTA-FEIRA')
elif dia == 6:
  print('SEXTA-FEIRA')
elif dia == 7:
  print('SÁBADO')
else:
  print('VALOR INVÁLIDO')